								<p><b>1. Makan beragam makanan secara proporsional dengan pola gizi seimbang dan lebih banyak daripada sebelum hamil.</b></p>  
								<p><b>2. Istirahat yang Cukup</b></p>  
									<ul class="list">
										<li>Tidur malam paling sedikit 6-7 jam dan usahakan siangnya tidur/berbaring 1-2 jam.</li>
										<li>Posisi tidur sebaiknya miring ke kiri.</li>
										<li>Pada daerah endemis malaria gunakan kelambu berinsektisida.</li>
										<li>Bersama dengan suami lakukan rangsangan/stimulasi pada janin dengan sering mengelus-elus perut ibu dan ajak janin bicara sejak usia kandungan 4 bulan.</li>
