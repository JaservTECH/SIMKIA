									<ul class="list">
										<li>Ibu hamil yang sehat dapat melakukan aktivitas fisik sehari-hari dengan memperhatikan kondisi ibu dan keamanan janin yang dikandungnya.</li>
										<li>Suami membantu istrinya yang sedang hamil untuk melakukan pekerjaan sehari-hari. </li>
