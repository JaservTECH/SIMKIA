								<p><b>3. Menjaga Kebersihan Diri</b></p>  
									<ul class="list">
										<li>Cuci tangan dengan sabun dan air bersih mengalir sebelum makan, setelah buang air besar dan buang air kecil.</li>
										<li>Menyikat gigi secara benar dan teratur minimal setelah sarapan dan sebelum tidur.</li>
										<li>Mandi 2 kali sehari</li>
										<li>Bersihkan payudara dan daerah kemaluan.</li>
										<li>Ganti pakaian dan pakaian dalam setiap hari.</li>
										<li>Periksakan gigi ke fasilitas kesehatan pada saat periksa kehamilan.</li>
									</ul>
								<p>4. Boleh melakukan hubungan suami istri selama hamil. Tanyakan ke petugas kesehatan cara yang aman.</p>
